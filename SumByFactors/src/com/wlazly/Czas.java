package com.wlazly;

public class Czas {
    int time1;
    int time2;
    int time3;


    public Czas(int time1, int time2, int time3) {
        this.time1 = time1;
        this.time2 = time2;
        this.time3 = time3;
    }

    public int getTime1() {
        return time1;
    }

    public void setTime1(int time1) {
        this.time1 = time1;
    }

    public int getTime2() {
        return time2;
    }

    public void setTime2(int time2) {
        this.time2 = time2;
    }

    public int getTime3() {
        return time3;
    }

    public void setTime3(int time3) {
        this.time3 = time3;
    }
}
